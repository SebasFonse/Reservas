package pq1;

public class SalaSeleccionada {
    private int numero;
    private int costo;

    public SalaSeleccionada(int numero, int costo) {
        this.numero = numero;
        this.costo = costo;
    }

    public int getNumero() {
        return numero;
    }

    public int getCosto() {
        return costo;
    }
}

